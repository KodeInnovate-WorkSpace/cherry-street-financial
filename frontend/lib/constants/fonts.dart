TextStyle poppinsRegular({
  double size = 12,
  Color color = Colors.black,
  double? letterSpacing,
}) {
  return TextStyle(
    fontFamily: "PoppinsRegular",
    fontSize: size,
    color: color,
    letterSpacing: letterSpacing,
  );
}

